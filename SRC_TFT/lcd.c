void LCD_GPIOInit(void)
{
	//Configure_GPO(PORT_CS, 	PIN_CS);
    Configure_GPO(PORT_CS, PIN_CS);
	Configure_GPO(PORT_DC, PIN_DC);
	Configure_GPO(PORT_RST, PIN_RST);
	Configure_GPO(PORT_LED, PIN_LED); 
    LCD_LED_SET;
    delay_ms(100);
    LCD_LED_CLR;
    
    
}

/*****************************************************************************
 * @name       :void LCD_RESET(void)
 * @date       :2018-08-09 
 * @function   :Reset LCD screen
 * @parameters :None
 * @retvalue   :None
******************************************************************************/	
void LCD_RESET(void)
{
	LCD_RST_CLR;
	delay_ms(100);	
	LCD_RST_SET;
	delay_ms(50);
}

